def _condition_label(meta): return f"Sample_{meta['group']}"
def _patient_data_dir(cfg,pid): return os.path.join(cfg.data_root,pid)
def _patient_out_dir(cfg,pid):  return os.path.join(cfg.base_out_dir,pid)
def _results_pkl_path(cfg,pid): return os.path.join(_patient_out_dir(cfg,pid),"results.pkl")

def _save_results_pkl(results,path):
    import pickle
    os.makedirs(os.path.dirname(path),exist_ok=True)
    with open(path,"wb") as f: pickle.dump(results,f,protocol=pickle.HIGHEST_PROTOCOL)

def load_patient_results(base_out_dir):
    import pickle; pr={}
    for meta in COHORT_METADATA:
        pid=meta["id"]
        path=os.path.join(base_out_dir,pid,"results.pkl")
        if not os.path.exists(path): continue
        try:
            with open(path,"rb") as f: pr[pid]=pickle.load(f)
        except Exception as e:
            print(f"  WARNING: {pid}: {e}")
    print(f"Loaded {len(pr)}/{len(COHORT_METADATA)} patients")
    return pr

def _build_analysis_config(cfg,pid):
    from spicem_kidney_4 import AnalysisConfig
    meta=COHORT_META_BY_ID[pid]
    return AnalysisConfig(
        base_dir=_patient_data_dir(cfg,pid),out_dir=_patient_out_dir(cfg,pid),
        conditions=[_condition_label(meta)],regions=cfg.regions,
        diffusion_coefficient=cfg.diffusion_coefficient,k_neighbors=cfg.k_neighbors,
        grid_resolution=cfg.grid_resolution,gaussian_sigma=cfg.gaussian_sigma,
        permutation_iters=cfg.permutation_iters,
        min_coupling_strength=cfg.min_coupling_strength,
        top_n_plots=cfg.top_n_plots,num_workers=cfg.num_workers,
        grid_pattern=cfg.grid_pattern,flux_folder=cfg.flux_folder)

