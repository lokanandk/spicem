def _apply_metadata_patch():
    try:
        import spicem_kidney_4.core as _core
        import json as _json, os as _os, pandas as _pd

        def _patched(base_dir,condition,region_idx,
                     grid_pattern="communities_hexagonal_Region_{}.json",
                     flux_folder="exchange_fluxes"):
            region_dir=_os.path.join(base_dir,condition,f"Region_{region_idx}")
            gj=_os.path.join(region_dir,grid_pattern.format(region_idx))
            if not _os.path.exists(gj):
                gj=_os.path.join(region_dir,f"communities_Region_{region_idx}.json")
            if not _os.path.exists(gj):
                raise FileNotFoundError(f"Grid JSON missing: {region_dir}")
            with open(gj) as fh: grids=_json.load(fh)
            mc=_os.path.join(region_dir,"metadata.csv")
            if not _os.path.exists(mc):
                raise FileNotFoundError(f"Metadata CSV missing: {mc}")
            df=_pd.read_csv(mc)
            rmap={}
            for col in ["pxl_col","x","coord_x","pos_x"]:
                if col in df.columns: rmap[col]="px_x"; break
            for col in ["pxl_row","y","coord_y","pos_y"]:
                if col in df.columns: rmap[col]="px_y"; break
            for col in ["Idents","Graph.based","celltype","CellType",
                        "cell_type_annotation","annotation"]:
                if col in df.columns: rmap[col]="cell_type"; break
            df=df.rename(columns=rmap)
            for req in ["px_x","px_y","cell_type"]:
                if req not in df.columns:
                    raise KeyError(f"metadata.csv missing: {req}")
            df=df.dropna(subset=["px_x","px_y","cell_type"]).copy()
            df["cell_type"]=df["cell_type"].astype(str)
            df["px_x"]=_pd.to_numeric(df["px_x"],errors="coerce")
            df["px_y"]=_pd.to_numeric(df["px_y"],errors="coerce")
            df=df.dropna(subset=["px_x","px_y"]).copy()
            if "barcode_clean" in df.columns: df=df.set_index("barcode_clean",drop=True)
            elif "barcode" in df.columns: df=df.set_index("barcode",drop=True)
            elif df.columns[0] not in ["px_x","px_y","cell_type"]:
                df=df.set_index(df.columns[0],drop=True)
            df.index=df.index.astype(str).str.strip()
            fd=_os.path.join(region_dir,flux_folder)
            if not _os.path.isdir(fd): raise FileNotFoundError(f"Flux dir missing: {fd}")
            return grids,df,fd
        _core.load_region_data=_patched
    except ImportError:
        pass


