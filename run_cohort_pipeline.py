    grid_pattern:  str="communities_hexagonal_Region_{}.json"
    flux_folder:   str="exchange_fluxes"
    diffusion_coefficient: float=0.1
    k_neighbors:   int=4
    grid_resolution: int=80
    gaussian_sigma: float=1.2
    permutation_iters: int=500
    min_coupling_strength: float=0.001
    top_n_plots:   int=20
    num_workers:   int=10
    random_seed:   int=42
    streamline_density: float=4.5
    streamline_lw_scale: float=2.0
    streamline_lw_base: float=0.6
    streamline_arrow_size: float=1.1
    streamline_maxlength: float=5.5
    streamline_minlength: float=0.2
    scatter_size_bg: int=14
    scatter_size_highlight: int=70
    scatter_alpha_bg: float=0.22
    scatter_alpha_highlight: float=0.82
    consensus_differential_coarsen_factor: int=2
    consensus_differential_density: float=3.2
    consensus_differential_top_n: int=10
    # Cohort-level settings
    run_cohort_consensus:   bool=True
    cohort_consensus_top_n: int=20
    clinical_fields:        List[str]=field(default_factory=lambda:["eGFR","fibrosis"])
    clinical_weight:        float=0.40
    ckd_weight:             float=0.60
    top_clinical_k:         int=5
