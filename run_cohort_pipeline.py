def run_single_patient(patient_id,cfg,dry_run=False):
    meta=COHORT_META_BY_ID.get(patient_id)
    if meta is None:
        return {"patient_id":patient_id,"status":"UNKNOWN_ID","error":"Not in cohort"}
    out_dir=_patient_out_dir(cfg,patient_id); cond=_condition_label(meta)
    row={"patient_id":patient_id,"group":meta["group"],
         "age":meta["age"],"sex":meta["sex"],
         "eGFR":meta["eGFR"],"fibrosis":meta["fibrosis"],
         "condition_label":cond,"status":"PENDING",
         "n_significant_mets":0,"n_plots_total":0,
         "elapsed_sec":0.0,"error":""}
    if cfg.skip_existing and os.path.exists(_results_pkl_path(cfg,patient_id)):
        row["status"]="SKIPPED"
        if cfg.verbose: print(f"  [{patient_id}] SKIP")
        return row
    if dry_run:
        cond_dir=os.path.join(_patient_data_dir(cfg,patient_id),cond)
        found=[r for r in cfg.regions
               if os.path.isdir(os.path.join(cond_dir,f"Region_{r}"))]
        row["status"]="DRY_RUN"
        if cfg.verbose: print(f"  [{patient_id}] DRY_RUN | {cond} | found={found}")
        return row
    t0=time.time()
    try:
        os.makedirs(out_dir,exist_ok=True)
        _apply_metadata_patch()
        from spicem_kidney_4 import run_analysis_pipeline,save_results_to_csv
        results=run_analysis_pipeline(_build_analysis_config(cfg,patient_id),
                                      verbose=cfg.verbose)
        row["n_significant_mets"]=len(getattr(results,"significant_mets",[]))
        save_results_to_csv(results,out_dir,verbose=cfg.verbose)
        _save_results_pkl(results,_results_pkl_path(cfg,patient_id))
        from plotting import generate_all_plots
        saved=generate_all_plots(results,cfg=_build_plot_config(cfg),
                                 verbose=cfg.verbose)
        row["n_plots_total"]=sum(len(v) for v in saved.values())
        try:
            from enhanced_plots import generate_all_enhanced_plots,generate_spotlight_report
            generate_all_enhanced_plots(results,cfg=_build_plot_config(cfg),
                                        verbose=cfg.verbose)
            generate_spotlight_report(results,cfg=_build_plot_config(cfg),
                                      extra_metabolites=cfg.spotlight_extra_mets,
                                      verbose=cfg.verbose)
        except Exception as e:
            if cfg.verbose: print(f"  [{patient_id}] WARNING enhanced: {e}")
        try:
            from plot_summarizer import create_comprehensive_summaries
            create_comprehensive_summaries(results,verbose=False)
        except Exception as e:
            if cfg.verbose: print(f"  [{patient_id}] WARNING summarizer: {e}")
        row["status"]="OK"
    except Exception as e:
        row["status"]="ERROR"; row["error"]=str(e)
        if cfg.verbose: print(f"  [{patient_id}] ERROR: {e}")
        traceback.print_exc()
    row["elapsed_sec"]=round(time.time()-t0,1)
    if cfg.verbose:
        print(f"  [{patient_id}] {row['status']}  t={row['elapsed_sec']}s  "
              f"mets={row['n_significant_mets']}")
    return row


def run_cohort(cfg=None,dry_run=False):
    if cfg is None: cfg=CohortConfig()
    patients=cfg.patients or [p["id"] for p in COHORT_METADATA]
    os.makedirs(cfg.base_out_dir,exist_ok=True)
    print("="*70)
    print("SPICEM COHORT PIPELINE  (v7)")
    print("="*70)
    print(f"Patients  : {len(patients)}")
    print(f"Outputs   :")
    print(f"  plots_consensus_cohort/   simple summary (violin+dot+donut+clinical)")
    print(f"  merged_plots/             3-panel streamline per metabolite")
    print(f"  cohort_comparison_wceg/   WCEG statistical comparison")
    if dry_run: print("MODE: DRY RUN")
    print()
    groups={}
    for pid in patients:
        g=COHORT_META_BY_ID.get(pid,{}).get("group","?")
        groups.setdefault(g,[]).append(pid)
    for g in sorted(groups): print(f"  {g:10s}: {', '.join(groups[g])}")
    print()

    rows=[]
    for i,pid in enumerate(patients,1):
        print(f"[{i}/{len(patients)}] {pid} ({COHORT_META_BY_ID[pid]['group']})")
        rows.append(run_single_patient(pid,cfg,dry_run=dry_run))
    summary_df=pd.DataFrame(rows)
    if not dry_run:
        summary_df.to_csv(os.path.join(cfg.base_out_dir,"cohort_summary.csv"),index=False)

    n_ok=int((summary_df["status"].isin(["OK","SKIPPED"])).sum())

    if n_ok>0 and not dry_run:
        pr=load_patient_results(cfg.base_out_dir)
        if not pr:
            print("  No patient results found — skipping cohort steps.")
            return summary_df

        # Build exchange records once — shared by all cohort steps
        exchange_df=None
        try:
            from consensus_exchange_network import build_exchange_records
            print("\n  Building exchange records (shared) …")
            exchange_df=build_exchange_records(pr)
            print(f"  {exchange_df['metabolite'].nunique()} metabolites")
        except Exception as e:
            print(f"  WARNING: exchange records failed: {e}")

        # ── Step 9A: Consensus summary plots ─────────────────────────────
        if cfg.run_cohort_consensus:
            print("\n"+"="*70)
            print("STEP 9A — CONSENSUS SUMMARY PLOTS (simple layout)")
            print("="*70)
            try:
                from cohort_consensus_plots import generate_cohort_consensus_plots
                cons_out=os.path.join(cfg.base_out_dir,"plots_consensus_cohort")
                saved,selected_mets,rank_df=generate_cohort_consensus_plots(
                    pr,out_dir=cons_out,
                    top_n=cfg.cohort_consensus_top_n,
                    clinical_fields=cfg.clinical_fields,
                    clinical_weight=cfg.clinical_weight,
                    ckd_weight=cfg.ckd_weight,
                    top_clinical_k=cfg.top_clinical_k,
                    verbose=cfg.verbose)
                print(f"\n  ✓ {len(saved)} consensus summary figures")
            except Exception as e:
                print(f"  ERROR: {e}"); traceback.print_exc()
                selected_mets=[]

        # ── Step 9B: Merged streamline plots (combined) ──────────────────
        if cfg.run_merged_plots_combined:
            print("\n"+"="*70)
            print("STEP 9B — MERGED STREAMLINE PLOTS (combined: CKD + clinical)")
            print("="*70)
            try:
                from merged_streamline_plots import generate_merged_streamline_plots_combined
                saved_c = generate_merged_streamline_plots_combined(
                    pr,
                    out_dir=os.path.join(cfg.base_out_dir,"merged_plots_combined"),
                    top_n=cfg.cohort_consensus_top_n,
                    clinical_fields=cfg.clinical_fields,
                    clinical_weight=cfg.clinical_weight,
                    ckd_weight=cfg.ckd_weight,
                    top_clinical_k=cfg.top_clinical_k,
                    top_exchanges=cfg.merged_top_exchanges,
                    p_floor=cfg.merged_combined_p_floor,
                    wceg_n_permutations=cfg.wceg_n_permutations,
                    wceg_alpha=cfg.wceg_alpha,
                    exchange_df=exchange_df,
                    cfg=_build_plot_config(cfg),
                    verbose=cfg.verbose)
                print(f"  ✓ {len(saved_c)} combined figures")
            except Exception as e:
                print(f"  ERROR: {e}"); traceback.print_exc()

        # ── Step 9C: WCEG group comparisons + volcano plots ───────────────
        print("\n"+"="*70)
        print("STEP 9C — WCEG GROUP COMPARISONS + VOLCANO PLOTS")
        print("="*70)
        comparison_results = {}
        try:
            from cohort_comparison import ComparisonConfig, run_full_cohort_comparison
            comp_cfg = ComparisonConfig(
                base_out_dir=cfg.base_out_dir,
                n_permutations=cfg.wceg_n_permutations,
                perm_seed=cfg.random_seed,
                p_thresh=0.05,
                verbose=cfg.verbose)
            comparison_results = run_full_cohort_comparison(
                pr, comp_cfg)
            print("  ✓ WCEG comparisons and volcano plots saved")
        except Exception as e:
            print(f"  ERROR: {e}"); traceback.print_exc()

        # ── Step 9D: Merged streamline plots for WCEG-significant mets ────
        if comparison_results:
            print("\n"+"="*70)
            print("STEP 9D — MERGED STREAMLINE PLOTS (WCEG-significant mets)")
            print("  Metabolites = significant hits from WCEG volcano")
            print("="*70)
            try:
                from merged_streamline_plots import (
                    generate_merged_streamline_plots_wceg_sig)
                saved_ws = generate_merged_streamline_plots_wceg_sig(
                    pr,
                    comparison_results=comparison_results,
                    out_dir=os.path.join(cfg.base_out_dir,"merged_plots_wceg_sig"),
                    wceg_n_permutations=cfg.wceg_n_permutations,
                    wceg_alpha=cfg.wceg_alpha,
                    exchange_df=exchange_df,
                    cfg=_build_plot_config(cfg),
                    top_metabolites=20,
                    top_exchanges=cfg.merged_top_exchanges,
                    verbose=cfg.verbose)
                total = sum(len(v) for v in saved_ws.values())
                print(f"  ✓ {total} WCEG-sig figures across {len(saved_ws)} comparisons")
            except Exception as e:
                print(f"  ERROR: {e}"); traceback.print_exc()

        # ── Step 9E: Clinical-correlation merged plots ─────────────────────
        if cfg.run_merged_plots_clinical:
            print("\n"+"="*70)
            print("STEP 9E — MERGED STREAMLINE PLOTS (clinical correlation)")
            print("  Method: Spearman rho + BH-FDR across all patients")
            print("="*70)
            try:
                from merged_streamline_plots import (
                    generate_merged_streamline_plots_clinical)
                saved_cl = generate_merged_streamline_plots_clinical(
                    pr,
                    out_dir=os.path.join(cfg.base_out_dir,
                                         "merged_plots_clinical"),
                    clinical_fields=cfg.clinical_fields,
                    padj_threshold=cfg.clinical_padj_threshold,
                    top_metabolites=cfg.cohort_consensus_top_n,
                    top_exchanges=cfg.merged_top_exchanges,
                    p_floor=cfg.merged_combined_p_floor,
                    exchange_df=exchange_df,
                    cfg=_build_plot_config(cfg),
                    verbose=cfg.verbose)
                print(f"  ✓ {len(saved_cl)} clinical-correlation figures")
            except Exception as e:
                print(f"  ERROR: {e}"); traceback.print_exc()

    print("\n"+"="*70+"  SUMMARY")
    for status,count in summary_df["status"].value_counts().items():
        print(f"  {status:15s}: {count}")
    return summary_df


def _parse_args():
    p=argparse.ArgumentParser(description="SPICEM cohort pipeline v7")
    p.add_argument("--data-root",      default="patient_data")
    p.add_argument("--out-dir",        default="cohort_output")
    p.add_argument("--patient",        nargs="+",default=None)
    p.add_argument("--regions",        nargs="+",type=int,default=[1,2,3,4,5])
    p.add_argument("--skip-existing",  action="store_true")
    p.add_argument("--dry-run",        action="store_true")
    p.add_argument("--workers",        type=int,default=10)
    p.add_argument("--perms",          type=int,default=500)
    p.add_argument("--seed",           type=int,default=42)
    p.add_argument("--top-n",          type=int,default=20)
    p.add_argument("--top-clinical-k", type=int,default=5)
    p.add_argument("--wceg-perms",     type=int,default=1000)
    p.add_argument("--wceg-alpha",     type=float,default=0.20)
    p.add_argument("--no-consensus",   action="store_true")
    p.add_argument("--no-merged",      action="store_true")
    p.add_argument("--no-merged-wceg", action="store_true",
                   help="Skip WCEG-weighted merged plots folder")
    p.add_argument("--no-merged-diff",  action="store_true",
                   help="Skip differential merged plots folder")
    p.add_argument("--no-merged-combined", action="store_true",
                   help="Skip combined merged plots folder")
    p.add_argument("--combined-p-floor",   type=float, default=0.5,
                   help="p_floor for combined score (default 0.5)")
    p.add_argument("--diff-top-mets",   type=int, default=20,
                   help="Top N significant metabolites per comparison (default 20)")
    p.add_argument("--merged-top-k",   type=int,default=5,
                   help="Max exchange rows per merged figure (default 5)")
    p.add_argument("--no-wceg",        action="store_true")
    p.add_argument("--use-corrected",  action="store_true")
    p.add_argument("--quiet",          action="store_true")
    return p.parse_args()

